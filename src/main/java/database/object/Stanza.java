package database.object;

public class Stanza {

	String gruppoStanze;
	String stanza;

	public String getGruppoStanze() {
		return gruppoStanze;
	}

	public void setGruppoStanze( String gruppoStanze ) {
		this.gruppoStanze = gruppoStanze;
	}

	public String getStanza() {
		return stanza;
	}

	public void setStanza( String stanza ) {
		this.stanza = stanza;
	}

}
